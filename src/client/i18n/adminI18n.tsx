const dictionaries = { en, zh };

interface AdminI18nContextValue {
  language: AdminLanguage;
  setLanguage: (language: AdminLanguage) => void;
  toggleLanguage: () => void;
  t: (key: string, values?: Record<string, string | number>) => string;
}

const AdminI18nContext = createContext<AdminI18nContextValue | null>(null);

function readInitialLanguage(): AdminLanguage {
  if (typeof window === "undefined") {
    return "en";
  }
  return window.localStorage.getItem(storageKey) === "zh" ? "zh" : "en";
}

export function AdminI18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<AdminLanguage>(() => readInitialLanguage());

  const value = useMemo<AdminI18nContextValue>(() => {
    function setLanguage(nextLanguage: AdminLanguage) {
      setLanguageState(nextLanguage);
      window.localStorage.setItem(storageKey, nextLanguage);
    }

    function t(key: string, values: Record<string, string | number> = {}) {
      const template = dictionaries[language][key] ?? en[key] ?? key;
      return Object.entries(values).reduce(
        (result, [name, replacement]) => result.replaceAll(`{{${name}}}`, String(replacement)),
        template,
      );
    }

    return {
      language,
      setLanguage,
      toggleLanguage: () => setLanguage(language === "en" ? "zh" : "en"),
      t,
    };
  }, [language]);

  return <AdminI18nContext.Provider value={value}>{children}</AdminI18nContext.Provider>;
}

export function useAdminI18n() {
  const context = useContext(AdminI18nContext);
  if (!context) {
    return {
      language: "en" as const,
      setLanguage: () => undefined,
      toggleLanguage: () => undefined,
      t: (key: string, values: Record<string, string | number> = {}) => {
        const template = en[key] ?? key;
        return Object.entries(values).reduce(
          (result, [name, replacement]) => result.replaceAll(`{{${name}}}`, String(replacement)),
          template,
        );
      },
    };
  }
  return context;
}

export function AdminLanguageToggle() {
  const { t, toggleLanguage } = useAdminI18n();

  return (
    <button className="button button--ghost admin-language-toggle" type="button" onClick={toggleLanguage}>
      {t("admin.languageToggle")}
    </button>
  );
}
